      function t(value) {
        return translateTextValue(value);
      }

      function translateElementAttributes(root) {
        const nodes = [root, ...Array.from(root.querySelectorAll?.("[placeholder], [title], [aria-label]") || [])];
        nodes.forEach((node) => {
          if (node.closest?.("[data-i18n-skip], .user-content")) return;
          ["placeholder", "title", "aria-label"].forEach((attr) => {
            if (!node.hasAttribute?.(attr)) return;
            const raw = node.getAttribute(attr);
            const translated = translateTextValue(raw);
            if (translated !== raw) node.setAttribute(attr, translated);
          });
        });
      }

      function translateTextNodes(root) {
        const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
          acceptNode(node) {
            const parent = node.parentElement;
            if (!parent || parent.closest("script, style, textarea, input")) return NodeFilter.FILTER_REJECT;
            if (parent.closest("[data-i18n-skip], .user-content, .crm-message-body, .message-body, .summary-line, .lead-fact-grid, .note-item .item-snippet")) return NodeFilter.FILTER_REJECT;
            if (!node.nodeValue || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
            return NodeFilter.FILTER_ACCEPT;
          },
        });
        const nodes = [];
        while (walker.nextNode()) nodes.push(walker.currentNode);
        nodes.forEach((node) => {
          const translated = translateTextValue(node.nodeValue);
          if (translated !== node.nodeValue) node.nodeValue = translated;
        });
      }

      function translatePage(root = document.body) {
        if (!root) return;
        translateElementAttributes(root);
        translateTextNodes(root);
        document.documentElement.lang = getUiLanguage();
        updateLanguageToggle();
      }

      function updateLanguageToggle() {
        const button = document.getElementById("topLanguageToggle");
        if (!button) return;
        const language = getUiLanguage();
        button.textContent = language.toUpperCase();
        button.setAttribute("aria-pressed", language === "fr" ? "true" : "false");
        button.setAttribute("title", language === "fr" ? "Basculer en anglais" : "Switch to French");
        button.setAttribute("aria-label", language === "fr" ? "Basculer en anglais" : "Switch to French");
      }

      function setUiLanguage(language) {
        const normalized = normalizeUiLanguage(language);
        localStorage.setItem("lead-ui-language", normalized);
        document.documentElement.lang = normalized;
        updateLanguageToggle();
        translatePage();
      }

      function installI18nObserver() {
        const observer = new MutationObserver((mutations) => {
          if (getUiLanguage() === "en") return;
          window.requestAnimationFrame(() => {
            mutations.forEach((mutation) => {
              mutation.addedNodes.forEach((node) => {
                if (node.nodeType === Node.ELEMENT_NODE) translatePage(node);
              });
            });
          });
        });
        observer.observe(document.body, { childList: true, subtree: true });
      }
